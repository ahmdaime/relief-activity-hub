// Fungsi untuk dapatkan negara rawak
export const getRandomCountry = (usedIndices: Set<number>): { country: Country; index: number } | null => {
    const availableIndices = COUNTRIES_DATA.map((_, idx) => idx)
        .filter(idx => !usedIndices.has(idx));

    if (availableIndices.length === 0) return null;

    const randomIndex = availableIndices[Math.floor(Math.random() * availableIndices.length)];
    return {
        country: COUNTRIES_DATA[randomIndex],
        index: randomIndex
    };
};

